package com.example.lab11.controller.task4;

public class Student {
    private String name;
    private int age;

    public Student() {
        this.name = "Joseph";
        this.age = 24;
    }

    public String getName() {
        return name;
    }

    public int getAge() {
        return age;
    }

    public void setAge(int age) {
        this.age = age;
    }
}
